/*
	1.3 网络核心

		分组交换

			在某种网络应用中，端系统彼此交换报文(message)。
			报文能够包含该应用的设计者需要的任何东西。
			报文可以执行一种控制功能(例如，图1-2所示例子中的“你好”报文)，
			也可以包含数据，例如电子邮件数据、JPEG图像或MP3音频文件。
			为了从源端系统向目的端系统发送一个报文，
			源将长报文划分为较小的数据块，称为分组(packet)。
			在源和目的地之间，每个分组都通过通信链路和分组交换机(packet switch)传送，
			交换机主要有 路由器(router) 和 链路层交换机(link-layer switch) 两类。
			分组以等于该链路最大传输速率的速度通过通信链路。
			因此，如果某源端系统或分组交换机经过一条链路发送一个 L bit 的分组，
			链路的传输速率为 R bps，则传输该分组的时间为 L/R s。

			( L ) 表示数据包的长度(以位为单位)。
			( R ) 表示传输速率(以每秒比特数为单位)。

		存储转发传输

			接收数据：当一个数据包（packet）到达某个节点（如路由器或链路层交换机）时，该节点会先接收整个数据包。
			存储数据：接收到的数据包会被暂时存储在节点的缓冲区中。
			处理数据：节点会对数据包进行必要的处理，例如检查错误、确定下一跳地址等。
			转发数据：处理完成后，节点会将数据包转发到下一个节点。

		电路交换 (Circuit Switching) 和 分组交换 (Packet Switching)

			电路交换 (Circuit Switching)
				定义：电路交换是一种通信方法，在通信开始之前，会在发送方和接收方之间建立一条专用的物理连接（电路）。

				特点：
					专用通道：一旦建立连接，这条电路在整个通信过程中都是专用的，不会被其他通信共享。
					固定带宽：连接建立后，带宽是固定的，不会因为没有数据传输而释放。
					低延迟：由于建立了专用通道，数据传输的延迟较低。
					资源利用率低：即使在没有数据传输时，电路仍然占用资源，导致资源利用率低。

				应用场景：
					电话系统：传统的电话通信系统就是典型的电路交换系统。当你拨打电话时，交换机会在你和对方之间建立一条专用电路，直到通话结束。


			分组交换 (Packet Switching)
				定义：分组交换是一种通信方法，数据被分割成多个小的数据包（packet），每个数据包独立地通过网络传输，到达目的地后再重新组装成原始数据。

				特点：
					无连接：数据包在传输前不需要建立专用通道，每个数据包可以独立选择路径。
					动态带宽：带宽是动态分配的，只有在有数据传输时才会占用资源。
					高资源利用率：由于资源是按需分配的，因此资源利用率较高。
					可能的延迟：数据包可能经过不同的路径，导致传输延迟和顺序错乱，需要在目的地重新排序。

				应用场景：
					互联网：现代互联网广泛采用分组交换技术。例如，当你浏览网页时，网页数据被分割成多个数据包，通过互联网传输到你的设备上，然后重新组装成完整的网页。

			对比
				资源利用：
					电路交换：资源利用率低，因为即使没有数据传输，电路仍然占用资源。
					分组交换：资源利用率高，因为资源是按需分配的。

				延迟：
					电路交换：低延迟，因为建立了专用通道。
					分组交换：可能有较高的延迟，因为数据包可能经过不同的路径。

				灵活性：
					电路交换：灵活性低，因为需要建立专用通道。
					分组交换：灵活性高，因为数据包可以独立选择路径。

				可靠性：
					电路交换：可靠性高，因为一旦建立连接，数据传输相对稳定。
					分组交换：可靠性可能较低，因为数据包可能丢失或顺序错乱，需要重传和重组。

			总结
				电路交换适用于需要低延迟和高可靠性的场景，如传统电话通信。
				分组交换适用于需要高资源利用率和灵活性的场景，如现代互联网通信。
*/