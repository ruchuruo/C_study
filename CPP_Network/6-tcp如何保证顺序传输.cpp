/*
	tcp如何保证顺序传输

	序号（Sequence Number）
		每个数据段都有一个序号：TCP 为每个数据段分配一个唯一的序号。
			发送方将数据分割成多个数据段，每个数据段都有一个序号。

		接收方按序号重组数据：接收方根据序号将接收到的数据段重新组合成原始的数据流。
			如果某个数据段丢失或延迟，接收方会等待该数据段到达后再继续重组。

	确认应答（Acknowledgment）
		确认应答机制：接收方在接收到数据段后，会发送一个确认应答（ACK）给发送方，确认已经成功接收到的数据段的序号。
		重传机制：如果发送方在一定时间内没有收到确认应答，会认为该数据段丢失，并重新发送该数据段。

	滑动窗口（Sliding Window）
		滑动窗口机制：TCP 使用滑动窗口机制来管理数据的发送和接收。
			发送方维护一个滑动窗口，表示可以连续发送而无需等待确认的数据段的数量。

		流量控制：接收方通过调整滑动窗口的大小来控制发送方的发送速率，防止接收方的缓冲区溢出。

	重排序（Reordering）
		数据段可能乱序到达：由于网络中的各种原因，数据段可能会乱序到达接收方。
		缓存和重组：接收方会缓存乱序到达的数据段，并在所有数据段到达后按序号重组。

	超时重传（Retransmission）
		超时机制：发送方为每个数据段设置一个超时时间，如果在超时时间内没有收到确认应答，会重新发送该数据段。
		拥塞控制：TCP 还通过拥塞控制算法来动态调整发送速率，避免网络拥塞。

	校验和（Checksum）
		校验和机制：每个数据段都包含一个校验和字段，用于检测数据在传输过程中是否发生错误。
		错误检测：接收方在接收到数据段后，会计算校验和并与数据段中的校验和进行比较，
			如果发现错误，会要求发送方重新发送该数据段。

	总结
		序号：每个数据段都有一个唯一的序号，接收方按序号重组数据。
		确认应答：接收方发送确认应答，发送方根据确认应答决定是否重传。
		滑动窗口：管理数据的发送和接收，实现流量控制。
		重排序：接收方缓存乱序到达的数据段，并按序号重组。
		超时重传：发送方在超时时间内没有收到确认应答时，重新发送数据段。
		校验和：检测数据传输过程中的错误，确保数据的完整性。
*/